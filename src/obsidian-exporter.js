import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';
import os from 'node:os';

class ObsidianExporter {
  constructor(memoryPath, options = {}) {
    this.memoryPath = memoryPath;
    this.vaultPath = path.join(process.cwd(), 'obsidian_vault');
    this.exportAll = options.all || false;
  }

  async export() {
    if (!fs.existsSync(this.memoryPath)) {
      console.warn(`[OBSIDIAN] Memory file not found: ${this.memoryPath}`);
      return;
    }

    const memory = JSON.parse(fs.readFileSync(this.memoryPath, 'utf-8'));
    
    if (memory.public_facing?.config?.vault_path) {
      let vp = memory.public_facing.config.vault_path;
      if (vp.startsWith('~/')) {
        vp = path.join(os.homedir(), vp.slice(2));
      }
      this.vaultPath = vp;
    }

    console.log(`[OBSIDIAN] Exporting memory to ${this.vaultPath}...`);
    
    const receipts = memory.receipts || [];

    this._ensureDir(this.vaultPath);
    this._ensureDir(path.join(this.vaultPath, 'Techniques'));
    this._ensureDir(path.join(this.vaultPath, 'Insights'));
    this._ensureDir(path.join(this.vaultPath, 'Failures'));
    this._ensureDir(path.join(this.vaultPath, 'General'));

    // Generate Agent Profile
    this._generateAgentProfile(memory);

    let exportedCount = 0;

    for (const receipt of receipts) {
      // Public filter: only export if public: true or exportAll is set
      if (!this.exportAll && receipt.public !== true) {
        continue;
      }

      const metadata = this._parseReceipt(receipt);
      const category = this._categorize(metadata, receipt.category);
      const fileName = `${metadata.hash.slice(0, 8)}.md`;
      const filePath = path.join(this.vaultPath, category, fileName);

      const content = this._generateMarkdown(metadata, receipt);
      fs.writeFileSync(filePath, content);
      exportedCount++;
    }

    console.log(`[OBSIDIAN] Exported ${exportedCount} notes.`);
    console.log(`[OBSIDIAN] Vault ready at: ${this.vaultPath}`);
  }

  _generateAgentProfile(memory) {
    const pf = memory.public_facing || {};
    const name = pf.name || 'Anonymous Agent';
    const config = pf.config || {};
    const profilePath = path.join(this.vaultPath, 'Agent-Profile.md');
    
    const content = `---
id: agent-profile
name: ${name}
type: sovereign-agent
status: active
format: ${config.format || 'obsidian'}
---

# Agent Profile: ${name}

This is the public identity and knowledge graph entry for a Swibe sovereign agent.

## Configuration
- **Sync Strategy:** ${config.sync_strategy || 'selective'}
- **Vault Path:** ${config.vault_path || 'default'}
- **Target:** ${config.target || 'obsidian'}

## Capabilities
- [x] Verifiable Receipt Chains
- [x] Public Insight Publishing
- [x] Hierarchical Memory (Working/Short/Long)

---
*Generated by Swibe Obsidian Bridge*
`;
    fs.writeFileSync(profilePath, content);
  }

  _ensureDir(dir) {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
    }
  }

  _parseReceipt(receipt) {
    let data = {};
    try {
      data = JSON.parse(receipt.prev);
    } catch (e) {
      data = { content: receipt.prev };
    }

    return {
      hash: receipt.hash,
      timestamp: receipt.timestamp,
      prompt: data.prompt || 'No prompt',
      content: data.content || '',
      prevHash: data.prev || null
    };
  }

  _categorize(metadata, explicitlySetCategory) {
    if (explicitlySetCategory && explicitlySetCategory !== 'general') {
      // Map to folders
      const lower = explicitlySetCategory.toLowerCase();
      if (lower.includes('tech')) return 'Techniques';
      if (lower.includes('insight')) return 'Insights';
      if (lower.includes('fail') || lower.includes('error')) return 'Failures';
      return 'General';
    }

    const content = metadata.content.toLowerCase();
    const prompt = metadata.prompt.toLowerCase();

    if (content.includes('error') || content.includes('failed') || content.includes('refuse')) {
      return 'Failures';
    }

    if (content.includes('fn ') || content.includes('class ') || content.includes('const ') || content.includes('function')) {
      return 'Techniques';
    }

    return 'Insights';
  }

  _generateMarkdown(metadata, receipt) {
    const date = new Date(metadata.timestamp).toISOString();
    const category = this._categorize(metadata, receipt.category);
    
    const yaml = `---
id: ${metadata.hash}
timestamp: ${date}
category: ${category}
hash: ${metadata.hash}
prev_receipt: ${metadata.prevHash || 'none'}
public: ${receipt.public || false}
tags: ${JSON.stringify(receipt.tags || [])}
---

# ${metadata.prompt.slice(0, 50)}${metadata.prompt.length > 50 ? '...' : ''}

**Timestamp:** ${date}
**Receipt Hash:** \`${metadata.hash}\`

## Prompt
> ${metadata.prompt}

## Content
${metadata.content}

${metadata.prevHash ? `## Citations
- Verifiable receipt: [swibe://receipt/${metadata.prevHash}](swibe://receipt/${metadata.prevHash})` : ''}

---
*Generated by Swibe Obsidian Bridge*
`;
    return yaml;
  }
}

export { ObsidianExporter };
